<p align="justify" class="style1"> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Anak yang sehat, cerdas, berpenampilan menarik, dan berakhlak mulia merupakan
dambaan setiap orang tua. Agar dapat mencapai hal tersebut terdapat berbagai kriteria yang
harus terpenuhi dalam pertumbuhan dan perkembangan anak, salah satunya adalah faktor
keturunan atau genetika. Namun, selain faktor keturunan masih terdapat faktor lain yang
mempengaruhi kualitas seorang anak.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kualitas seorang anak dapat dinilai dari proses tumbuh kembang. Proses tumbuh
kembang merupakan hasil interaksi faktor genetik dan faktor lingkungan. Faktor
genetik/keturunan adalah faktor yang berhubungan dengan gen yang berasal dari ayah dan
ibu, sedangkan faktor lingkungan meliputi lingkungan biologis, fisik, psikologis, dan sosial.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pertumbuhan dan perkembangan mengalami peningkatan yang pesat pada usia dini,
yaitu dari 0 sampai 5 tahun. Masa ini sering juga disebut sebagai fase ”Golden Age”. Golden
age merupakan masa yang sangat penting untuk memperhatikan tumbuh kembang anak
secara cermat agar sedini mungkin dapat terdeteksi apabila terjadi kelainan. Selain itu,
penanganan kelainan yang sesuai pada masa golden age dapat meminimalisir kelainan
pertumbuhan dan perkembangan anak sehingga kelaianan yang bersifat permanen dapat
dicegah.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pemantauan tumbuh kembang anak meliputi pemantauan dari aspek fisik, psikologi,
dan sosial. Pemantauan tersebut harus dilakukan secara teratur dan berkesinambungan.
Sedini mungkin pemantauan dapat dilakukan oleh orang tua. Selain itu pemantauan juga
dapat dilakukan oleh masyarakat melalui kegiatan posyandu dan oleh guru di sekolah. Oleh
karena itu, pengetahuan tentang deteksi dini pertumbuhan dan perkembangan anak perlu
dimiliki oleh orang tua, guru, dan masyarakat.

</p><br>
<strong>1. Tahap Pertumbuhan dan Perkembangan Anak</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Tumbuh kembang anak berlangsung secara teratur, saling berkaitan, dan
berkesinambungan dimulai sejak pembuahan sampai dewasa. Walaupun terdapat variasi,
namun setiap anak akan melewati suatu pola tertentu. Tanuwijaya (2003) memaparkan
tentang tahapan tumbuh kembang anak yang terbagi menjadi dua, yaitu masa pranatal dan
masa postnatal. Setiap masa tersebut memiliki ciri khas dan perbedaan dalam anatomi,
fisiologi, biokimia, dan karakternya.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Masa pranatal adalah masa kehidupan janin di dalam kandungan. Masa ini dibagi
menjadi dua periode, yaitu masa embrio dan masa fetus. Masa embrio adalah masa sejak
konsepsi sampai umur kehamilan 8 minggu, sedangkan masa fetus adalah sejak umur 9
minggu sampai kelahiran.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Masa postnatal atau masa setelah lahir terdiri dari lima periode. Periode pertama
adalah masa neonatal dimana bayi berusia 0 - 28 hari dilanjutkan masa bayi yaitu sampai
usia 2 tahun. Masa prasekolah adalah masa anak berusia 2 – 6 tahun. Sampai dengan
masa ini, anak laki-laki dan perempuan belum terdapat perbedaan, namun ketika masuk
dalam masa selanjutnya yaitu masa sekolah atau masa pubertas, perempuan berusia 6 – 10
tahun, sedangkan laki-laki berusia 8 - 12 tahun. Anak perempuan memasuki masa
adolensensi atau masa remaja lebih awal dibanding anak laki-laki, yaitu pada usia 10 tahun
dan berakhir lebih cepat pada usia 18 tahun. Anak laki-laki memulai masa pubertasa pada
usia 12 tahun dan berakhir pada usia 20 tahun.<br>
<strong>2. Faktor-faktor yang mempengaruhi Pertumbuhan dan Perkembangan</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Banyak faktor yang mempengaruhi pertumbuhan dan perkembangan anak. Secara
garis besar faktor-faktor tersebut dapat dibagi menjadi 2 golongan, yaitu faktor dalam
(internal) dan faktor luar (eksternal/lingkungan). Pertumbuhan dan perkembangan
merupakan hasil interaksi dua faktor tersebut.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Faktor internal terdiri dari perbedaan ras/etnik atau bangsa, keluarga, umur, jenis
kelamin, kelainan genetik, dan kelainan kromosom. Anak yang terlahir dari suatu ras
tertentu, misalnya ras Eropa mempunyai ukuran tungkai yang lebih panjang daripada ras
Mongol. Wanita lebih cepat dewasa dibanding laki-laki. Pada masa pubertas wanita umumnya tumbuh lebih cepat daripada laki-laki, kemudian setelah melewati masa pubertas
sebalinya laki-laki akan tumbuh lebih cepat. Adanya suatu kelainan genetik dan kromosom
dapat mempengaruhi pertumbuhan dan perkembangan anak, seperti yang terlihat pada
anak yang menderita Sindroma Down.
<br>
<strong>3. Pertumbuhan dan Perkembangan Anak</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pertumbuhan organ-organ tubuh mengikuti 4 pola, yaitu pola umum, neural, limfoid,
serta reproduksi. Organ-organ yang mengikuti pola umum adalah tulang panjang, otot
skelet, sistem pencernaan, pernafasan, peredaran darah, volume darah. Perkembangan
otak bersama tulang-tulang yang melindunginya, mata, dan telinga berlangsung lebih dini.
Otak bayi yang baru dilahirkan telah mempunyai berat 25% berat otak dewasa, 75% berat
otak dewasa pada umur 2 tahun, dan pada umur 10 tahun telah mencapai 95% berat otak
dewasa.
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pertumbuhan jaringan limfoid agak berbeda dengan dari bagian tubuh lainnya,
pertumbuhan mencapai maksimum sebelum remaja kemudian menurun hingga mencapai
ukuran dewasa. Sedangkan organ-organ reproduksi tumbuh mengikuti pola tersendiri, yaitu
pertumbuhan lambat pada usia pra remaja, kemudian disusul pacu tumbuh pesat pada usia
remaja. (Tanuwijaya, 2003; Meadow & Newell, 2002; Cameron, 2002 ). Perbedaan empat
pola pertumbuhan tersebut tergambar dalam kurva di bawah ini.
