<strong>4. Aspek kemampuan fungsional, yaitu motorik kasar, motorik halus dan penglihatan,</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Berbicara dan bahasa, serta sosial emosi dan perilaku. Jika terjadi kekurangan pada salah
