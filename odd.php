<p align="center"><strong>ODD (Oppositional Defiant Disorder)</strong><br /></p>
<p class="style1"><strong>Pengertian ODD</strong><br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ODD atau oppositional defiant disorder adalah gangguan perilaku yang sering muncul di masa kanak-kanak dengan gejala berupa mudah marah dan tersinggung. Penderita ODD juga kerap menunjukkan sikap membangkang dan mendendam.
  <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ODD lebih dari sekadar tantrum yang normal terlihat pada anak-anak. Tantrum timbul sebagai respons ketika keinginan anak tidak terpenuhi. Biasanya, tantrum muncul di usia 1–1,5 tahun, kemudian memburuk di usia 2–3 tahun, dan mereda pada usia 4 tahun.
  Sedangkan ODD biasanya muncul di usia 6–8 tahun, tetapi bisa berlangsung sampai remaja bahkan dewasa. Gejala yang ditunjukkan juga lebih agresif dan lebih sering muncul dibanding tantrum, sehingga menimbulkan dampak negatif pada keseharian penderitanya.
<p class="style1"><strong>Penyebab dan Faktor Risiko ODD</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Penyebab ODD belum diketahui secara pasti. Namun, ada dugaan ODD terkait dengan faktor lingkungan, biologis, dan psikologis. Beberapa faktor biologis yang dipercaya dapat memicu ODD adalah:</p>
  <ul type="disc" class="style1">
    <li>Menderita kelainan fungsi otak, seperti fungsi neurotransmiter yang tidak normal</li>
    <li>Mengalami cedera pada otak yang menyebabkan gangguan di bagian otak yang berfungsi memberikan penilaian</li>
    <li>Memiliki orang tua dengan riwayat ADHD, gangguan bipolar, depresi, conduct disorder, atau penyalahgunaan NAPZA</li>
    <li>Memiliki ibu yang merokok saat hamil</li>
    <li>Menderita kekurangan nutrisi</li>  
  </ul>
<p class="style1">Berikut ini adalah beberapa faktor psikologis yang dipercaya dapat memicu ODD:</p>
    <ul type="disc" class="style1">
        <li>Ketidakharmonisan dalam keluarga</li>
        <li>Kurangnya perhatian dari orang tua</li>
        <li>Ketidakmampuan menjalin hubungan sosial</li>
    </ul>
<p class="style1">Sedangkan faktor lingkungan yang diduga dapat memicu ODD antara lain:</p>
    <ul type="disc" class="style1">
        <li>Kemiskinan</li>
        <li>Pelecehan</li>
        <li>Tinggal di lingkungan yang tidak baik atau penuh kekerasan</li>
    </ul>
<p class="style1"><strong>Gejala ODD</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Penentangan dan pembangkangan merupakan perilaku yang normal muncul pada masa perkembangan anak. Tetapi, pada anak yang menderita ODD, perilaku yang senang membangkang ini akan lebih parah dan berlangsung lebih lama, sedikitnya selama 6 bulan.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Biasanya, gejala ODD terlihat sebelum anak memasuki masa sekolah, namun bisa juga muncul menjelang masa remaja. Gejala tersebut dapat menyebabkan gangguan di lingkungan keluarga, sekolah, maupun sosial.
Gejala oppositional defiant disorder bisa terlihat pada perilaku dan emosi penderita, yang dapat dikenali dari beberapa perilaku berikut:</p>
    <ul type="disc" class="style1">
        <li>Mudah hilang kesabaran</li>
        <li>Mudah marah, kesal dan tersinggung</li>
        <li>Sangat sensitif dan mudah merasa terganggu</li>
        <li>Sering mengganggu dan membuat marah orang lain</li>
        <li>Sering berdebat dengan orang yang lebih tua</li>
        <li>Kerap menolak mematuhi perintah atau peraturan</li>
        <li>Sering menyalahkan orang lain atas kesalahannya sendiri</li>
        <li>Sering menunjukkan dendam atau kebencian pada orang lain</li>
    </ul>