<p align="center"><strong>Conduct disorder (CD)</strong><br /></p>
<p class="style1"><strong>Pengertian Conduct disorder (CD)</strong><br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Conduct disorder adalah gangguan perilaku dan emosi serius yang membuat anak menunjukkan perilaku kekerasan, suka merusak benda tertentu, dan cenderung sulit mengikuti aturan di sekolah maupun di rumah.
Anak yang memiliki gangguan perilaku jenis ini biasanya gemar berbohong dan menipu, bahkan tidak segan melakukan perbuatan yang melanggar hukum, seperti vandalisme, berkelahi, atau melukai orang lain. Anak-anak yang memilki gangguan perilaku conduct disorder juga mungkin suka menyiksa hewan.
  
 <p class="style1"><strong>Pengobatan</strong><br>
  <ul type="I" class="style1">
    <li>Untuk menentukan jenis gangguan perilaku, psikiater dan psikolog dapat melakukan pemeriksaan psikologi pada anak. Setelah jenis gangguan perilaku pada anak diketahui, ia mungkin akan mendapatkan penanganan berupa psikoterapi, terapi bermain, atau mendapatkan obat-obatan, jika diperlukan.</li>
    <li>Mendidik anak dengan gangguan perilaku memang tidak mudah. Para orang tua yang anaknya memiliki gangguan perilaku perlu bersabar dan memberikan perhatian serta kasih sayang lebih kepada anak. Jangan ragu untuk meminta saran dari psikolog atau psikiater terkait cara tepat mendidik dan membimbing anak dengan gangguan ini.</li>
  </ul>
