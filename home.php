<p><strong>Gangguan Perilaku pada Anak yang Tidak Boleh Diabaikan</strong><br />
    <strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><span class="style3">Gangguan perilaku pada anak adalah suatu kondisi ketika anak sering berperilaku menyimpang dan di luar batas, sehingga berpotensi merugikan dirinya sendiri dan orang lain. Anak yang memiliki gangguan perilaku sering kali dianggap nakal dan bahkan agresif.<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  &nbsp;Setiap anak memiliki karakter yang berbeda. Terkadang, anak bisa tampak baik dan menggemaskan. Namun, ada kalanya anak bisa terlihat nakal dan mengganggu. Akan tetapi, Bunda dan Ayah perlu waspada jika Si Kecil sering melakukan kenakalan yang jauh lebih parah dan berbeda dari anak-anak lain yang sebaya.

Seorang anak bisa dikatakan mengalami gangguan perilaku jika ia memiliki pola tingkah laku yang menyimpang dan berulang atau menetap selama lebih dari 6 bulan.
<br />
<strong>Gejala dan Tanda Gangguan Perilaku pada Anak</strong><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  &nbsp;Gangguan perilaku pada anak umumnya bisa terlihat ketika anak sudah bersekolah. Akan tetapi, pada kasus tertentu, gangguan perilaku juga bisa terlihat ketika anak berusia lebih muda, misalnya ketika masih balita.
Anak dengan gangguan perilaku biasanya akan menunjukkan pola tingkah laku sebagai berikut:<br>
  <strong>1. Mudah marah atau sulit menahan emosi</strong><br />
  <strong>2. Impulsif atau sulit menahan dorongan untuk melakukan sesuatu</strong><br />
  <strong>3. Sering membantah atau melawan orang lain, misalnya orang tua, kakak, atau guru di sekolahnya</strong><br />
  <strong>4. Kerap melakukan kekerasan, baik secara fisik maupun verbal, pada orang lain atau hewan</strong><br />
  <strong>5. Sering mengejek orang lain atau perilaku bullying, berkelahi, dan berbuat onar</strong><br />
  <strong>6. Gemar melempar dan merusak barang saat marah</strong><br />
  <strong>7. Sering melakukan perbuatan yang kurang baik, misalnya mencuri dan berbohong, dan malas belajar </strong><br>
  <strong>8. Sering melanggar aturan sekolah seperti sering bolos sekolah, merokok, atau bahkan minum minuman beralkohol dan menggunakan narkoba
 </strong><br>
