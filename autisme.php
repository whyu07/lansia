<p align="center"><strong>Autisme</strong><br /></p>
<p class="style1"><strong>Pengertian Autisme</strong><br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Autisme merupakan gangguan perkembangan fungsi otak. Gangguan ini mencakup bidang sosial dan fungsi afek, komunikasi verbal (bahasa) dan non verbal, imajinasi, fleksibilitas, lingkup interest (minat), kognisi dan atensi.
  Ada beberapa perilaku yang sering dilaporkan oleh orang tua dengan anak autisme. Seperti keterlambatan berbicara dari anak-anak sepantarannya, perilaku acuh dan tak acuh, atau cemas jika anaknya dicurigai tuli.
  Kebiasaan yang di luar perilaku normal ini biasanya sudah terlihat saat anak berusia 3 tahun. Pada saat-saat inilah biasanya orang tua menyadari bahwa ada yang berbeda pada anak mereka.
<p class="style1"><strong>Diagnosis Autisme</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pemeriksaan fisik yang bisa ditemukan pada pasien dengan autisme adalah adanya gejala autisme. Seorang anak akan diperiksa mulai dari tinggi dan dan berat badan, hingga pemeriksaan tubuh secara general untuk melihat adakah kelainan atau tidak.<br>
<br> Beberapa pemeriksaan yang akan dilakukan dokter sebelum memberikan diagnosis antara lain:
  <ul type="disc" class="style1">
    <li>Pemeriksaan perilaku anak</li>
    <li>Pemeriksaan fisik (termasuk pemeriksaan pendengaran)</li>
    <li>Pemeriksaan lanjutan (misalnya: pemeriksaan kromosom, pemeriksaan EEG, dan MRI)</li>
    <li>Pemeriksaan perkembangan anak seperti M-CHAT</li>
  </ul>
<p class="style1"><strong>Gejala Autisme</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Gejala pada anak autisme biasanya sudah terlihat sebelum anak berusia 3 tahun. Beberapa gejala yang bisa diperhatikan antara lain tidak adanya kontak mata dan tidak adanya respons terhadap lingkungan.
Jika tidak dilakukan terapi, maka setelah usia 3 tahun perkembangan anak akan berhenti atau mundur. Seperti tidak mengenal suara orang tuanya dan tidak mengenali namanya.<br><br>
Beberapa pakar mengungkapkan 3 gejala pada penderita autisme klasik, yaitu:</p>
  <ul type="disc" class="style1">
    <li>Gangguan interaksi sosial</li>
    <li>Hambatan dalam komunikasi ucapan dan bukan ucapan (bahasa tubuh dan isyarat)</li>
    <li>Kegiatan dan minat yang aneh atau sangat terbatas.</li>
  </ul>
<p class="style1">Selain itu juga terdapat sifat-sifat lainnya yang biasa ditemukan pada anak autisme, seperti:</p>
  <ul type="disc" class="style1">
    <li>Sulit bergabung dengan anak-anak yang lain</li>
    <li>Tertawa atau cekikikan tidak pada tempatnya</li>
    <li>Menghindari kontak mata atau hanya sedikit melakukan kontak mata</li>
    <li>Menunjukkan ketidakpekaan terhadap nyeri</li>
    <li>Lebih senang menyendiri atau menarik diri dari pergaulan</li>
    <li>Tidak membentuk hubungan pribadi yang terbuka</li>
    <li>Gemar memutar benda atau terpaku pada benda tertentu</li>
    <li>Sangat tergantung kepada benda yang sudah dikenalnya dengan baik secara fisik</li>
    <li>Aktif atau justru tidak aktif sama sekali</li>
    <li>Tidak memberikan respons terhadap cara pengajaran yang normal</li>
    <li>Tertarik pada hal-hal yang serupa, tidak mau menerima atau mengalami perubahan</li>
    <li>Tidak takut akan bahaya</li>
    <li>Terpaku pada permainan yang ganjil</li>
    <li>Ekolalia (mengulang kata-kata atau suku kata)</li>
    <li>Tidak mau dipeluk</li>
    <li>Tidak memberikan respon terhadap kata-kata, bersikap seolah-olah tuli</li>
    <li>Mengalami kesulitan dalam mengungkapkan kebutuhannya melalui kata-kata</li>
    <li>Senang meminta melalui isyarat tangan atau menunjuk</li>
    <li>Jengkel atau kesal membabi buta</li>
  </ul>
<p class="style1"><strong>Pengobatan Autisme</strong></p>
<ul type="I" class="style1">
  <li>Edukasi kepada keluarga</li>
  Keluarga memiliki peran yang penting dalam membantu perkembangan anak. Bagaimanapun juga, orang tua adalah orang terdekat yang dapat membantu anak untuk belajar berkomunikasi, berperilaku terhadap lingkungan dan orang sekitar. Bisa dibilang keluarga adalah jendela bagi penderita autisme untuk masuk ke dunia luar. Meski perlu diakui bahwa ini bukanlah hal yang mudah.
  <li>Penggunaan obat-obatan</li>
  Penggunaan obat-obatan pada penderita autisme harus dibawah pengawasan dokter. Pengobatan ini diberikan jika dicurigai terdapat gangguan di otak yang mengganggu pusat emosi dari penderita autisme. Hal ini seringkali menimbulkan gangguan emosi mendadak, agresifitas, hiperaktif dan stereotipik. Beberapa obat yang bisa diberikan adalah haloperidol (antipsikotik), fenfluramin, naltrexone (antiopiat), clompramin (mengurangi kejang dan perilaku agresif).

</ul>